            <h3 class="text-center" style="padding-bottom:10px; ">Who We Are</h3>
            <p style="line-height:200%;">In 2004 Renee Beeker, a mother of six, founded the National Family Court Watch Project in Milford, Michigan.<br>
            <div></div>
The group launched a pilot effort by eight volunteers who observed 201 hearings held by 24 judges in California, Massachusetts, Michigan, New York and Rhode Island. Since the pilot, Renee has traveled the nation to speak about the project and recruit volunteers to expand into all 50 states.<br>
<div style="padding:5px;"></div>
The National Family Court Watch Project believes that large-scale data will reveal national trends and ignite a call for change in the family court system. The project is a conversation springboard and a "quiet observer" to get a sense of what's happening in the family courts, report those findings publicly through a new conduit, and work with judges and the public to find solutions.<br>
<div style="padding:5px;"></div>
Volunteers attend hearings and flag problems such as litigants without legal representation, language barriers, representation not being prepared or hearings postponed. Court watchers are often paralegal's, law students, senior citizens, student interns from various programs of study, as well as university professors offering this project to their students as a class project. Additionally organization wanting to work on a community effort have volunteered for this project.<br>
<div style="padding:5px;"></div>
The NFCWP is the first known program to use a standardized observational instrument nationwide. We observe and report trends that the data collection reveals. The ultimate goal is to work with the public and judicial system to create methods and procedures that help resolve the problems we are finding in family courts across the country. </p>
